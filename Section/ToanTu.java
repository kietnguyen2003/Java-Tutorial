package Section;

public class ToanTu {
  public static void main(String[] args) {
    int a = 5;
    int b = 3;
    System.out.println("a = " + a + ", b = " + b);
    System.out.println("a + b = " + (a + b));
    System.out.println("a - b = " + (a - b));
    System.out.println("a * b = " + (a * b));
    System.out.println("a / b = " + (a / b));
    System.out.println("a % b = " + (a % b));
    System.out.println("a++ = " + (a++));
    System.out.println("b-- = " + (b--));
    System.out.println("a > b = " + (a > b));
    System.out.println("a < b = " + (a < b));
    System.out.println("a >= b = " + (a >= b));
    System.out.println("a <= b = " + (a <= b));
    System.out.println("a == b = " + (a == b));
    System.out.println("a != b = " + (a != b));
  }
}
